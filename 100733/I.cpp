const int INF = 2000000000;

struct Edge {
    int from, to, cap, flow, index;
    Edge(int from, int to, int cap, int flow, int index) :
        from(from), to(to), cap(cap), flow(flow), index(index) {}
};

struct Dinic {
    int N;
    vector<vector<Edge> > G;
    vector<Edge *> dad;
    vector<int> Q;

    Dinic(int N) : N(N), G(N), dad(N), Q(N) {}

    void AddEdge(int from, int to, int cap) {
        G[from].push_back(Edge(from, to, cap, 0, G[to].size()));
        if (from == to) G[from].back().index++;
        G[to].push_back(Edge(to, from, 0, 0, G[from].size() - 1));
    }

    long long BlockingFlow(int s, int t) {
        fill(dad.begin(), dad.end(), (Edge *) NULL);
        dad[s] = &G[0][0] - 1;

        int head = 0, tail = 0;
        Q[tail++] = s;
        while (head < tail) {
            int x = Q[head++];
            for (int i = 0; i < G[x].size(); i++) {
                Edge &e = G[x][i];
                if (!dad[e.to] && e.cap - e.flow > 0) {
                    dad[e.to] = &G[x][i];
                    Q[tail++] = e.to;
                }
            }
        }
        if (!dad[t]) return 0;

        long long totflow = 0;
        for (int i = 0; i < G[t].size(); i++) {
            Edge *start = &G[G[t][i].to][G[t][i].index];
            int amt = INF;
            for (Edge *e = start; amt && e != dad[s]; e = dad[e->from]) {
                if (!e) { amt = 0; break; }
                amt = min(amt, e->cap - e->flow);
            }
            if (amt == 0) continue;
            for (Edge *e = start; amt && e != dad[s]; e = dad[e->from]) {
                e->flow += amt;
                G[e->to][e->index].flow -= amt;
            }
            totflow += amt;
        }
        return totflow;
    }

    long long GetMaxFlow(int s, int t) {
        long long totflow = 0;
        while (long long flow = BlockingFlow(s, t))
            totflow += flow;
        return totflow;
    }
};

int solve(VI l, VI r, int m, int t){
    sort(all(l));
    sort(all(r));

    int na=l.size(),nb=r.size();
    int N=2*na+2*nb+2;
    Dinic flow(N);

    for(int i=0; i<na; i++){
        if(i>=na-m)flow.AddEdge(0, i+1, 1);
        flow.AddEdge(i+1, na+i+1, 1);
    }

    for(int i=0; i<na; i++)
        for(int j=0; j<nb; j++){
            if(abs(l[i]-r[j])>=t)continue;
            flow.AddEdge(na+i+1, 2*na+j+1, 1);
            flow.AddEdge(2*na+nb+j+1, i+1, 1);
        }

    for(int i=0; i<nb; i++){
        if(i<m)flow.AddEdge(2*na+nb+i+1, N-1, 1);
        flow.AddEdge(2*na+i+1, 2*na+nb+i+1, 1);
    }

    int p=flow.GetMaxFlow(0, N-1);
    if(p>=m)return 1;
    return 0;
}

